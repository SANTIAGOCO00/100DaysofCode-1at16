# Nos dara la posición de un string especifico (¡LOS ESPACIOS VACIOS CUENTAN!)
print(len("Hello"[0])) # Salida H que equivale a la posición 0

# Inicia ahora de izquierda a derecha
print(len("Hello"[-3])) # Salida e que equivale a la posición -3

# Uniendo cadenas de Strings
print("Hola" + " " + "Santiago") # Salida: Hola Santiago

# Sumando Enteros
print(12+45)

# podemos udar _ para simular las comas en números largos. Ej: 1,000,000 (Las comas en Python son para seprar acciones)
print(1_000_000) # Salida 1000000

# Números float (float point) son aquellos que llevan decimales separados por puntos (.)
print(3.43564)

# Números Bool (boolean - buleanos) True y False
print(True)
print(False)
